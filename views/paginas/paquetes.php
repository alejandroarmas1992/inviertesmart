      <td>Rendimiento</td>
      <td>
      <ul>
        <li>Renta variable: Cuando se desconoce la ganancia o tasa de interés, un ejemplo son las acciones que veremos más adelante.</li>
        <li>Renta fija: Se conoce preliminarmente el plazo y ganancia o tasa de interés que pagará esa inversión. Un ejemplo son las obligaciones corporativas.</li>
        
    </ul>
      </td>
      
    </tr>
