      <td>
      <ul>
            <li>Corto Plazo: duración de la inversión de menos de 359 días, desde la fecha de inicio hasta la fecha de vencimiento.</li>
            <li>Largo plazo: duración de la inversión de más de 359 días desde la fecha de inicio hasta la fecha de vencimiento.</li>
            <li>Indefinido: No hay una fecha de vencimiento definida.</li>
         </ul>
      </td>
      
    </tr>
